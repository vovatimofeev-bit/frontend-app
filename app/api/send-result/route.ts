import { NextResponse } from "next/server";
import nodemailer from "nodemailer";
import { Buffer } from "buffer";

const YOUR_EMAIL = "bes8158@gmail.com";

export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { email, version, metrics } = body;

    if (!metrics || !Array.isArray(metrics)) {
      return NextResponse.json(
        { status: "error", message: "Нет метрик" },
        { status: 400 }
      );
    }

    const avg = (arr: number[]) =>
      arr.length ? arr.reduce((a, b) => a + b, 0) / arr.length : 0;

    const rmsValues = metrics.map((m: any) => Number(m.voiceRmsAvg ?? 0));
    const timeValues = metrics.map((m: any) => Number(m.responseTimeMs ?? 0));

    const avgRms = avg(rmsValues);
    const avgTime = avg(timeValues);

    const getState = (m: any) => {
      const rms = Number(m.voiceRmsAvg ?? 0);
      const time = Number(m.responseTimeMs ?? 0);

      if (rms > avgRms * 1.4) {
        return "ЭМОЦИОНАЛЬНОЕ НАПРЯЖЕНИЕ";
      }

      if (time > avgTime * 1.3) {
        return "ВЗВЕШЕННАЯ РАЗМЫШЛЯЮЩАЯ РЕАКЦИЯ";
      }

      return "СПОКОЙНАЯ УВЕРЕННАЯ РЕАКЦИЯ";
    };

    let calm = 0;
    let reflective = 0;
    let tense = 0;

    metrics.forEach((m: any) => {
      const state = getState(m);
      if (state === "СПОКОЙНАЯ УВЕРЕННАЯ РЕАКЦИЯ") calm++;
      if (state === "ВЗВЕШЕННАЯ РАЗМЫШЛЯЮЩАЯ РЕАКЦИЯ") reflective++;
      if (state === "ЭМОЦИОНАЛЬНОЕ НАПРЯЖЕНИЕ") tense++;
    });

    const seen = new Set<number>();
    const uniqueMetrics = metrics.filter((m: any) => {
      const idx = m.questionIndex;
      if (seen.has(idx)) return false;
      seen.add(idx);
      return true;
    });

    let text = `ПСИХОЭМОЦИОНАЛЬНЫЙ ПРОФИЛЬ РЕСПОНДЕНТА\n\n`;

    // ===== ИНФОРМАЦИЯ О РЕСПОНДЕНТЕ =====
    text += `=== ДАННЫЕ РЕСПОНДЕНТА ===\n`;
    text += `Email: ${email || "не указан"}\n`;
    text += `Тип теста: ${version || "не указан"}\n\n`;

    // ===== ГЛАВНЫЙ ВЫВОД =====
    text += `=== ГЛАВНЫЙ ВЫВОД ===\n`;

    if (tense > reflective && tense > calm) {
      text += `Профиль указывает на личность с высокой эмоциональной глубиной и чувствительностью. Реакции демонстрируют сильную вовлечённость, интенсивное проживание значимых тем и выраженную внутреннюю реактивность. Такой человек редко остаётся эмоционально равнодушным и глубоко переживает значимые события.\n`;
    } else if (reflective > calm) {
      text += `Профиль демонстрирует выраженный аналитический и вдумчивый стиль. Респондент склонен обдумывать решения, контролировать импульсы и оценивать последствия перед тем, как реагировать. Это говорит о зрелом, рациональном и осознанном подходе к жизненным ситуациям.\n`;
    } else {
      text += `Респондент демонстрирует высокий уровень эмоциональной устойчивости, внутреннего равновесия и самоконтроля. Реакции стабильные, без резких эмоциональных всплесков, что характерно для людей с уверенным, собранным и психологически устойчивым профилем.\n`;
    }

    // ===== СТАТИСТИКА =====
    text += `\n=== ЭМОЦИОНАЛЬНЫЙ СРЕЗ ===\n`;
    text += `Спокойные реакции: ${calm}\n`;
    text += `Взвешенные размышляющие реакции: ${reflective}\n`;
    text += `Эмоционально напряжённые реакции: ${tense}\n`;

    // ===== ОБЩИЙ ПОЧЕРК =====
    text += `\n=== ОБЩИЙ ЭМОЦИОНАЛЬНЫЙ ПОЧЕРК ===\n`;

    if (tense < calm && tense < reflective) {
      text += `Общий профиль характеризуется как устойчивый, собранный и рациональный, с редкими эмоциональными всплесками. Респондент, как правило, сохраняет самообладание, действует продуманно и демонстрирует высокий уровень саморегуляции.\n`;
    } else if (tense > calm) {
      text += `Профиль указывает на повышенную чувствительность к эмоционально значимым темам. Возможны выраженные реакции на личные триггеры, сильное внутреннее вовлечение и глубокое переживание отдельных ситуаций.\n`;
    } else {
      text += `Профиль демонстрирует сбалансированное сочетание эмоциональной устойчивости и вдумчивости. Человек способен сохранять контроль, анализировать происходящее и осознанно регулировать своё эмоциональное состояние.\n`;
    }

    // ===== СИЛЬНЫЕ СТОРОНЫ =====
    text += `\n=== СИЛЬНЫЕ СТОРОНЫ ПРОФИЛЯ ===\n`;

    if (calm > reflective && calm > tense) {
      text += `— высокая эмоциональная устойчивость\n— уверенность в реакциях\n— способность сохранять спокойствие под давлением\n— низкий уровень импульсивности\n`;
    } else if (reflective > calm) {
      text += `— развитое аналитическое мышление\n— умение взвешивать решения\n— самоконтроль и продуманность реакций\n— склонность к осознанным действиям\n`;
    } else {
      text += `— высокая эмоциональная глубина\n— сильная вовлечённость в происходящее\n— развитая эмпатия\n— интенсивное проживание значимых тем\n`;
    }

    // ===== ЗОНЫ РИСКА =====
    text += `\n=== ПОТЕНЦИАЛЬНЫЕ ЗОНЫ РИСКА ===\n`;

    if (tense > 0) {
      text += `Зафиксированы эпизоды эмоционального напряжения, что может указывать на чувствительные темы, внутренние триггеры или зоны повышенной личной значимости. В отдельных ситуациях возможны усиленные эмоциональные реакции.\n`;
    } else {
      text += `Выраженных признаков эмоциональной перегрузки не выявлено. Профиль выглядит устойчивым, сбалансированным и психологически стабильным.\n`;
    }

    // ===== АНАЛИЗ ПО ВОПРОСАМ =====
    text += `\n=== АНАЛИЗ РЕАКЦИЙ ПО ВОПРОСАМ ===\n\n`;

    uniqueMetrics.forEach((m: any) => {
      const state = getState(m);
      text += `Вопрос ${Number(m.questionIndex) + 1}\n`;
      text += `Тип реакции: ${state}\n`;

      if (state === "ЭМОЦИОНАЛЬНОЕ НАПРЯЖЕНИЕ") {
        text += `Интерпретация: вопрос затронул эмоционально значимую или личностно чувствительную область. Возможны скрытые переживания, внутренняя вовлечённость или личный эмоциональный отклик.\n`;
      } else if (state === "ВЗВЕШЕННАЯ РАЗМЫШЛЯЮЩАЯ РЕАКЦИЯ") {
        text += `Интерпретация: ответ формировался осознанно и взвешенно. Реакция отражает аналитический стиль мышления, стремление к точности, самоконтроль и осторожность в формулировках.\n`;
      } else {
        text += `Интерпретация: реакция спокойная и устойчивая. Вопрос не вызвал внутреннего конфликта, что указывает на уверенность, психологический комфорт и отсутствие напряжения по данной теме.\n`;
      }

      text += `\n`;
    });

    // ===== ЗАЩИТНЫЕ РЕАКЦИИ =====
    text += `\n=== ВОЗМОЖНЫЕ ЗАЩИТНЫЕ РЕАКЦИИ ===\n`;

    let foundFlags = false;

    uniqueMetrics.forEach((m: any) => {
      const rms = Number(m.voiceRmsAvg ?? 0);
      const time = Number(m.responseTimeMs ?? 0);

      if (rms > avgRms * 1.5 && time < avgTime * 0.7) {
        foundFlags = true;
        text += `Вопрос ${Number(m.questionIndex) + 1}: зафиксировано сочетание высокой эмоциональной реакции и быстрого ответа — возможный признак защитной, импульсивной или напряжённой реакции.\n`;
      }
    });

    if (!foundFlags) {
      text += `Явных признаков защитных реакций, искажения ответов или внутреннего сопротивления не выявлено.\n`;
    }

    // ===== ПСИХОЛОГИЧЕСКИЙ ПОРТРЕТ =====
    text += `\n=== ПСИХОЛОГИЧЕСКИЙ ПОРТРЕТ ===\n`;

    if (tense > calm && tense > reflective) {
      text += `Тип личности: эмоционально глубокий, чувствительный, интенсивно переживающий. Такой человек склонен глубоко проживать значимые события, обладает развитой эмпатией и сильной внутренней реакцией на личные темы.\n`;
    } else if (reflective > calm) {
      text += `Тип личности: рациональный, вдумчивый, аналитичный. Респондент склонен контролировать эмоции, обдумывать решения и действовать осознанно, избегая импульсивности.\n`;
    } else {
      text += `Тип личности: устойчивый, уравновешенный, уверенный. Реакции говорят о зрелой саморегуляции, внутренней стабильности и хорошем эмоциональном балансе.\n`;
    }

    // ===== ФИНАЛ =====
    text += `\n=== ФИНАЛЬНЫЙ ВЫВОД ===\n`;

    text += `Этот результат отражает уникальный эмоционально-поведенческий почерк респондента — индивидуальный стиль мышления, реагирования на стресс, принятия решений и проживания значимых тем.\n`;

    text += `Отчёт основан на анализе динамики реальных реакций, а не абстрактных шаблонов. Он может использоваться как инструмент самопонимания, личного роста, развития эмоциональной осознанности и более точного управления своим внутренним состоянием.\n`;

    const reportBuffer = Buffer.from(text, "utf-8");

    const transporter = nodemailer.createTransport({
      host: process.env.SMTP_HOST,
      port: Number(process.env.SMTP_PORT) || 587,
      secure: false,
      auth: {
        user: process.env.SMTP_USER,
        pass: process.env.SMTP_PASS
      }
    });

    await transporter.sendMail({
      from: `"Poligramm Test" <${process.env.SMTP_USER}>`,
      to: YOUR_EMAIL,
      subject: `Новый результат теста ${version}`,
      text: `Email респондента: ${email}`,
      attachments: [
        {
          filename: `result-${version}.txt`,
          content: reportBuffer
        }
      ]
    });

    return NextResponse.json({ status: "ok" });

  } catch (err) {
    console.error("SERVER ERROR:", err);
    return NextResponse.json(
      { status: "error", message: "Ошибка сервера" },
      { status: 500 }
    );
  }
}
